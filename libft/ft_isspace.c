#include "libft.h"

int	ft_isspace(int c)
{
	unsigned char ch;

	ch = (unsigned char)c;
	if (ch == ' ' || ch == '\t' || ch == '\n' || ch == '\v' || ch == '\f' ||
		ch == '\r')
		return (1);
	return (0);
}
