#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*tmp;

	tmp = *lst;
	while (tmp)
	{
		del(tmp->content);
		tmp = tmp->next;
		free(*lst);
		*lst = tmp;
	}
	lst = NULL;
}
