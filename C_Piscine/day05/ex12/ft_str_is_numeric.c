int	ft_str_is_numeric(char *str)
{
	int	flag;
	int	i;

	flag = 1;
	i = 0;
	while (str[i] != '\0')
	{
		if(str[i] > '9' || str[i] < '0')
		 flag = 0;
		i++;
	}
	return (flag);
}
