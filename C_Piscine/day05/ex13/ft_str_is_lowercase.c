 int ft_str_is_lowercase(char *str)
	{
		int	flag;
		int	i;

		flag = 1;
		i = 0;
		while (str[i] != '\0')
		{
			if(str[i] < 'a' || str[i] >'z')
			{
				flag = 0;
				break;
			}
			i++;
		}
		return (flag);
	}
