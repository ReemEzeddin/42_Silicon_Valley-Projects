int	ft_str_is_printable(char *str)
{
	int	flag;
	int	i;

	flag = 0;
	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] < 32 || str[i] > 127)
		{
			flag = 0;
			break ;
		}
		i++;
	}
	return (flag);
}
